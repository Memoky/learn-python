L = ['A', 'B', 'C', 'D', 'E']

print('L[0:4] =', L[0:4])
print('L[:4] =', L[:4])
print('L[1:3] =', L[1:3])
print('L[-2:] =', L[-2:])

M = list(range(20))

print('M[0:20] =',M[0:20])
print('M[:20] =',M[:20])
print('M[::5] =',M[::5])
print('M[:10:2] =', M[:10:2])
print('M[::-2] =', M[::-2])
print('M[-10:] =', M[-10::-1])




